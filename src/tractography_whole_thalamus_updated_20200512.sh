#   DWIPRE-PNC_V1 OUTPUTS - b0_mask.nii.gz, b0_mean.nii.gz
# 	FREESURFER STRUCTURAL - nu.mgz
#   FREESURFER SKULL-STRIPPED STRUCTURAL - norm.mgz
#	FREESURFER MASKS - aparc.DKTatlas+aseg.mgz, ThalamicNuclei.v10.T1.FSvoxelSpace.mgz
# 	YEO ROIS - Yeo7_split.nii.gz, Yeo17_split.nii.gz (in MNI 1mm space)
#   TARGETS LISTS - TARGETS_FS6_L.txt, TARGETS_FS6_R.txt, TARGETS_FS10_L.txt, TARGETS_FS10_L.txt, TARGETS_YEO7_L.txt, TARGETS_YEO7_R.txt, TARGETS_YEO17_L.txt, TARGETS_YEO17_R.txt

# 3. CONVERT FREESURFER MGZ FILES TO NII.GZ

# 4. CREATE SKULL-STRIPPED B0

# 5. CREATE FLIRT XFORMS
#    MNI_1mm TO NORM
#    MNI_2mm TO NORM

# 6. CREATE EPI_REG XFORMS
#    NORM TO B0

# 7. CREATE FLIRT+EPI_REG XFORMS
#    CONCATENATE MNI_1mm TO NORM TO B0
#    CONCATENATE MNI_2mm TO NORM TO B0

# 8. APPLY XFORMS TO FREESURFER
#    COREG FREESURFER NORM BRAIN TO B0
#   COREG DKT ATLAS TO B0
#   COREG THALAMUS TO B0
#   WARP B0 TO FREESURFER NORM TO MNI_1mm
#   WARP B0 TO FREESURFER NORM TO MNI_2mm

# 9. RESLICE YEO MASKS TO MNI152 1mm

# 10. APPLY XFORMS TO YEO
#   WARP YEO7 TO B0
#   WARP YEO17 TO B0

# 11. CREATE L/R FREESURFER THALAMUS, WM, CORTICAL, STOP, AND AVOID MASKS

# 12. CREATE L/R YEO CORTICAL, STOP, AND AVOID MASKS

# 13. PROBABILISTIC TRACTOGRAPHY
#   FREESURFER THALAMUS TO FREESURFER CORTICAL L/R
#   FREESURFER THALAMUS TO YEO7 CORTICAL L/R
#   FREESURFER THALAMUS TO YEO17 CORTICAL L/R




### OUTPUTS
#	--THALAMUS_2_FS6 (6 FREESURFER CORTICAL REGIONS)
#   --THALAMUS_2_FS10 (10 FREESURFER CORTICAL REGIONS)
#   --THALAMUS_2_YEO7 (7 YEO CORTICAL REGIONS)
#   --THALAMUS_2_YEO17 (17 YEO CORTICAL REGIONS)
#   --BIGGEST (6 FREESURFER SEGMENTATIONS OF THE THALAMUS)
#   --BIGGEST (10 FREESURFER SEGMENTATIONS OF THE THALAMUS)










